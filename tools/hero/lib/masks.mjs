export function maskSvg(name, { width, height }) {
  if (!layerNames.includes(name)) throw new Error(`Unknown hero layer: ${name}`)
  const sx = width / 1672
  const sy = height / 941
  const opening = `<svg width="${width}" height="${height}" viewBox="0 0 ${width} ${height}" xmlns="http://www.w3.org/2000/svg"><g transform="scale(${sx} ${sy})" fill="white">`
  if (name === 'van') {
    return `${opening}<defs><mask id="van-body" maskUnits="userSpaceOnUse"><rect width="1672" height="941" fill="black"/>${vanBody}<ellipse cx="1168" cy="597" rx="60" ry="86" fill="black"/><ellipse cx="1472" cy="574" rx="41" ry="68" fill="black"/></mask></defs><rect width="1672" height="941" mask="url(#van-body)"/></g></svg>`
  }
  return `${opening}${shapes[name]}</g></svg>`
}
